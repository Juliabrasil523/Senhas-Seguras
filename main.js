    if (entropia > 11){
        forcaSenha.classList.add('forte');
    } else if (entropia > 6 && entropia < 12) {
        forcaSenha.classList.add('media');
    } else if (entropia <= 5){
        forcaSenha.classList.add('fraca');
    }
